import base64

ARMOR_BEGIN = '-----BEGIN PGP PUBLIC KEY BLOCK-----\n'
ARMOR_END = '\n-----END PGP PUBLIC KEY BLOCK-----'
def asc2bin(asc):
    asc = asc[asc.find(ARMOR_BEGIN)+len(ARMOR_BEGIN):asc.find(ARMOR_END)]
    if asc.startswith('Version: '):
        asc = asc[asc.find('\n')+1:]
    return base64.b64decode(asc)

